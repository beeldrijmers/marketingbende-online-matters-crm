create table public.deals (
    id bigint generated by default as identity primary key,
    name text not null,
    company_id bigint,
    contact_ids bigint[],
    category text,
    stage text not null,
    description text,
    amount bigint,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now(),
    archived_at timestamp with time zone,
    expected_closing_date date,
    start_date date,
    delivery_date date,
    sales_id bigint,
    index smallint,
    trello_card_id text,
    -- The sales users this deal is assigned to. A deal is only visible to its
    -- assignees (RLS in 05_policies.sql), so this is the access-control list for
    -- the card: shared work gets several assignees, private cards just one.
    -- Defaults to the owner (sales_id) on insert via set_sales_id_default.
    assignee_ids bigint[] not null default '{}'::bigint[],
    -- "In de wacht" IS a kanban column (stage 'on-hold'). This flag is DERIVED
    -- from that stage by the sync_deal_on_hold trigger (on_hold := stage =
    -- 'on-hold') and drives the badge on the card; a direct write to it is
    -- overwritten by the trigger on the next save.
    on_hold boolean not null default false,
    -- Internal work (Happr product, Marketingbende/Online Matters own projects)
    -- vs external client work. Drives the Intern/Extern board filter.
    is_internal boolean not null default false,
    -- Set the first time the deal's Trello card moves into "Klaar", so the
    -- team-lead notification is sent at most once even though Trello webhooks
    -- are delivered at-least-once and retried.
    won_notified_at timestamp with time zone,
    moneybird_estimate_id text,
    moneybird_estimate_status text,
    moneybird_estimate_claimed_at timestamp with time zone,
    moneybird_estimate_created_by bigint,
    moneybird_estimate_error text,
    -- The Moneybird administration the document was created in. Connections are
    -- per user since the per-user Moneybird integration, so the administration
    -- differs per creator; the deep link in the UI needs it.
    moneybird_estimate_administration_id text,
    moneybird_invoice_id text,
    moneybird_invoice_status text,
    moneybird_invoice_claimed_at timestamp with time zone,
    moneybird_invoice_created_by bigint,
    moneybird_invoice_error text,
    moneybird_invoice_administration_id text,
    -- Whether the deal's amount is a monthly recurring fee ('maandelijks', e.g.
    -- an SEO subscription) or a single one-off project fee ('eenmalig', e.g. a
    -- webshop build). Drives the revenue dashboard (MRR vs one-off). Null when
    -- unknown / not applicable (Happr and other non-billable cards).
    revenue_period text,
    constraint deals_moneybird_estimate_status_check check (moneybird_estimate_status in ('pending', 'completed', 'failed')),
    constraint deals_moneybird_invoice_status_check check (moneybird_invoice_status in ('pending', 'completed', 'failed')),
    constraint deals_revenue_period_check check (revenue_period in ('maandelijks', 'eenmalig'))
);

