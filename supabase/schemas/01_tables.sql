-- Idempotency ledger for the Resend inbound-email webhook. Resend/Svix deliver
-- at-least-once, so a redelivered or retried event must not re-process the same
-- mail (which would create duplicate contact/deal notes). The webhook claims
-- an email_id here before processing; a conflicting insert means "already
-- handled". Only the edge function (service_role) touches it.
create table public.inbound_email_events (
    email_id text primary key,
    processed_at timestamp with time zone not null default now()
);

-- Durable health history for external integrations. Edge functions create and
-- finish these rows; authenticated CRM users only read them on the dashboard.
-- Keeping the last runs in Postgres makes failures visible after a page reload
-- instead of only showing a temporary toast to the person who pressed sync.
create table public.integration_runs (
    id bigint generated by default as identity primary key,
    integration text not null check (length(trim(integration)) > 0),
    run_kind text not null check (run_kind in ('manual', 'backfill')),
    status text not null check (status in ('running', 'success', 'partial', 'failed')),
    started_at timestamp with time zone not null default now(),
    finished_at timestamp with time zone,
    duration_ms integer check (duration_ms is null or duration_ms >= 0),
    items_processed integer not null default 0 check (items_processed >= 0),
    failed_count integer not null default 0 check (failed_count >= 0),
    summary jsonb not null default '{}'::jsonb,
    error text
);

create index integration_runs_integration_started_idx
    on public.integration_runs using btree (integration, started_at desc);

