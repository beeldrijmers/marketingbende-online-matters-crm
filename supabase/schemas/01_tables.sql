create table public.tasks (
    id bigint generated by default as identity primary key,
    -- A task is attached to a contact, a deal, or both. Trello-synced deal
    -- steps have no contact, so contact_id is nullable; the check below keeps
    -- every task anchored to at least one of the two.
    contact_id bigint,
    deal_id bigint,
    type text,
    text text,
    due_date timestamp with time zone,
    done_date timestamp with time zone,
    sales_id bigint,
    -- Where the task originated: a person ('manual'), a mirrored Trello
    -- checklist item ('trello'), or the CRM's next-action guard ('auto').
    -- Trello-sourced tasks are kept in sync with, and written back to, their card.
    source text not null default 'manual',
    -- The Trello checklist item this task mirrors (null for manual tasks). Used
    -- as the idempotency key for the sync and to write completions back.
    trello_checkitem_id text,
    constraint tasks_source_check check (source in ('manual', 'trello', 'auto')),
    constraint tasks_contact_or_deal_check
        check (contact_id is not null or deal_id is not null)
);

create table public.configuration (
    id integer not null default 1 primary key,
    config jsonb not null default '{}'::jsonb,
    constraint configuration_singleton check (id = 1)
);

-- Per-user Moneybird connection. Each CRM user links their OWN Moneybird
-- administration with a personal API token. The token is encrypted with
-- AES-256-GCM by the moneybird_connection edge function (key: the
-- MONEYBIRD_ENC_KEY function secret) before it is stored, and is NEVER
-- readable by client roles (see 06_grants.sql); only the edge functions
-- (service role) decrypt it.
create table public.moneybird_connections (
    id bigint generated by default as identity primary key,
    sales_id bigint not null,
    administration_id text not null,
    -- Display name of the administration at connect time (cosmetic, shown in
    -- the profile UI; the id is the source of truth).
    administration_name text not null default '',
    api_token_encrypted text not null,
    created_at timestamp with time zone not null default now(),
    updated_at timestamp with time zone not null default now()
);

create unique index uq__moneybird_connections__sales_id on public.moneybird_connections using btree (sales_id);

-- Cache of the Moneybird contact created for a CRM company, PER administration.
-- A Moneybird contact id is only valid inside the administration it was created
-- in, and every user connects their own administration, so one company can map
-- to a different contact per administration. Written exclusively by the
-- moneybird edge functions (service role).
create table public.moneybird_company_contacts (
    id bigint generated by default as identity primary key,
    company_id bigint not null,
    administration_id text not null,
    contact_id text not null,
    created_at timestamp with time zone not null default now()
);

create unique index uq__moneybird_company_contacts__company_administration
    on public.moneybird_company_contacts using btree (company_id, administration_id);

create table public.favicons_excluded_domains (
    id bigint generated by default as identity primary key,
    domain text not null
);

